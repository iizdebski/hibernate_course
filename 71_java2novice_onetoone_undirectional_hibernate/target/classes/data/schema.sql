DROP DATABASE IF EXISTS JAVA2NOTICEONETOONE;
CREATE DATABASE IF NOT EXISTS JAVA2NOTICEONETOONE CHARACTER SET utf8 COLLATE utf8_general_ci;

USE JAVA2NOTICEONETOONE;

DROP TABLE IF EXISTS EMPLOYEES;

CREATE TABLE EMPLOYEES (

                         EMP_ID BIGINT NOT NULL AUTO_INCREMENT,
                         NAME VARCHAR(252),
                         DEPARTMENT VARCHAR(128),
                         SALARY BIGINT,
                         JOINED_ON TIMESTAMP,
                         PRIMARY KEY (EMP_ID)
);

DROP TABLE IF EXISTS EMP_DETAILS;

CREATE TABLE EMP_DETAILS (

                           ED_ID BIGINT NOT NULL AUTO_INCREMENT,
                           EMP_ID BIGINT,
                           ADDRESS VARCHAR(252),
                           GENDER VARCHAR(8),
                           YEARS_OF_SERVICE BIGINT,
                           BANK_ACCOUNT VARCHAR(128),
                           PRIMARY KEY (ED_ID),
                           FOREIGN KEY (EMP_ID) REFERENCES EMPLOYEES(EMP_ID)
);

