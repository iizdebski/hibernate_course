DROP DATABASE IF EXISTS manytomanymapping;
CREATE DATABASE IF NOT EXISTS manytomanymapping CHARACTER SET utf8 COLLATE utf8_general_ci;
USE manytomanymapping;
DROP TABLE IF EXISTS ADDRESS;
CREATE TABLE ADDRESS(

                      ADDRESS_ID INTEGER NOT NULL AUTO_INCREMENT,
                      STREET_NAME VARCHAR(50),
                      CITY_NAME VARCHAR(50),
                      STATE_NAME VARCHAR(255),
                      PIN_CODE INTEGER,
                      PRIMARY KEY (ADDRESS_ID)
)
  ENGINE = innoDB
  DEFAULT CHARACTER SET = utf8
;

CREATE TABLE PROJECTS (

                        PR_ID BIGINT NOT NULL AUTO_INCREMENT,
                        NAME VARCHAR(252),
                        OWNER VARCHAR(252),
                        PRIMARY KEY (PR_ID)
);

DROP TABLE IF EXISTS EMPLOYEE;
CREATE TABLE EMPLOYEE(
                       EMPLOYEE_ID INTEGER NOT NULL AUTO_INCREMENT,
                       ADDRESS_ID INTEGER,
                       PR_ID BIGINT,
                       PRIMARY KEY (EMPLOYEE_ID),
                       FOREIGN KEY(ADDRESS_ID) REFERENCES ADDRESS(ADDRESS_ID),
                       FOREIGN KEY (PR_ID) REFERENCES PROJECTS(PR_ID)
)
  ENGINE = innoDB
  DEFAULT CHARACTER SET = utf8
;

